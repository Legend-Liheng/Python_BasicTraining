#减法
result2 = a - b
print("减法结果为：%d" % result2)

#乘法
result3 = a * b
print("乘法结果为：%d" % result3)

#除法
result4 = a / b
print("除法结果为：%f" % result4)

#整除
result5 = a // b
print("整除结果为：%d" % result5)

#取余
result6 = a % b
print("取余结果为：%d" % result6)

#指数
result7 = a ** b
print("指数结果为：%d" % result7)



